package com.itheima;

public class ForTest01 {
    public static void main(String[] args) {

        //需求：输出1-5
        for (int i =1;i<=5;i++){
            System.out.println(i);
        }
        System.out.println("--------");
        for (int i =5; i>=1;i--){
            System.out.println(i);
        }

    }
}
